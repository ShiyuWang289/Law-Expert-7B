def create_corpus():
    """将语料写入文件"""
    os.makedirs(CORPUS_DIR, exist_ok=True)

    for filename, content in CORPUS.items():
        filepath = os.path.join(CORPUS_DIR, filename)
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"✅ 已创建: {filepath} ({len(content)} 字)")

    # 写入元信息
    meta = {
        "files": list(CORPUS.keys()),
        "total_chars": sum(len(c) for c in CORPUS.values()),
        "description": "法律RAG语料库，覆盖劳动法、工伤、民法典、消保法核心条款"
    }
    with open(os.path.join(CORPUS_DIR, "meta.json"), "w", encoding="utf-8") as f:
        json.dump(meta, f, ensure_ascii=False, indent=2)

    print(f"\n📚 语料库创建完成")
    print(f"   文件数: {len(CORPUS)}")
    print(f"   总字数: {meta['total_chars']}")


if __name__ == "__main__":
    create_corpus()
